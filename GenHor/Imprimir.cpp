	std::cout << "| 03:00 - 04:00 |" << horas[0][3] << "|" << horas[1][3] << "|" << horas[0][3] << "|" << horas[1][3] << "|" << horas[1][3] << "|\n";
	std::cout << "+---------------+-----+-----+-----+-----+-----+\n";
	std::cout << "| 04:00 - 05:00 |" << horas[0][4] << "|" << horas[1][4] << "|" << horas[0][4] << "|" << horas[0][4] << "|" << horas[1][4] << "|\n";
	std::cout << "+---------------+-----+-----+-----+-----+-----+\n";
	std::cout << "| 05:00 - 06:00 |" << horas[0][4] << "|" << horas[1][4] << "|" << horas[0][4] << "|" << horas[1][4] << "|" << horas[1][4] << "|\n";
	std::cout << "+---------------+-----+-----+-----+-----+-----+\n";

